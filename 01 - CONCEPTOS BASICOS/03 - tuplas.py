# LAS TUPLAS
### Es una extructura de datos muy similar a una lista, pero no se puede modificar una vez creada
# # Se utiliza para almacenar datos que sabemos que no se van a modificar
###

# Se crea con ()
tupla = (1, "Pedro", 52)


# Por ejemplo si intentamos agregar un elemento a la tupla nos da error al ejecutar
# Si fuera una lista no daria error
